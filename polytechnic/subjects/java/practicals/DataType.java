public class DataType
{
		int 	age 	= 	25;               		// integer
		
		float 	cgpe	= 	7.3f;    				// floating
		
		char 	grade 	= 	'B';         			// character
		
		boolean pass 	= 	true;       			// boolean
		
		String 	subject = 	"Java Programming"; 	// String 
		
	
		public static void main(String arg[])
		{
				DataType obj	=	new DataType();
				
				System.out.println(obj.subject);
		}
}
