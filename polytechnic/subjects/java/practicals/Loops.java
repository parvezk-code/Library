public class Loops
{
		public static void main(String arg[])
		{
				int i;
				
				i=1;
				while(  i < 11  )
				{
					System.out.println(i);
					i++;
				}
				
				
				for( i=1; i<11; i++)
				{
					System.out.println(i);
				}
				
				
				i=1;
				do
				{
					System.out.println(i);
					i++;
				}
				while(  i < 11  );

		}
}
