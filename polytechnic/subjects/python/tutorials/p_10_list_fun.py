# append() fun roll list me ek value aakhir me jood dega aur roll me XXX items ho jaiange.
#5 append()  function
roll = ["a"]       #5.1 ["a"]
roll.append("b")   #5.2 ["a", "b"]
roll.append("c")   #5.3 ["a", "b", "c"]
roll.append("d")   #5.4 ["a", "b", "c", "d"]

# remove() fun roll list me ek value hata dega aur roll me XXX items ho jaiange.
#6 remove()  function
roll = ["a", "b", "c", "d"] #6.1
roll.remove("b")  #6.2  ["a", "c", "d"]
roll.remove("c")  #6.3  ["a", d"]


#7 copy(), sort(), reverse()  function
# copy fun ki sahayta se lt1 ko lt2 me copy kar rahe hai.
lt1 = [30, 40, 10, 20] #7.1
lt2 = lt1.copy()    #7.2  [30, 40, 10, 20]
lt2.reverse()       #7.3  [20,10,40,30]
lt2.sort()          #7.4  [10,20,30,40]

#9 index() function
