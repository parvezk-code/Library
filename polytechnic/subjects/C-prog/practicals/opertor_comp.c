/*
    Comparision operators
    ==      <       >
    !=      <=      >=  
*/

#include<stdio.h>

int main()
{
        int x = 5;
        int y = 3;

        printf( "%d \n",   x > y);

        printf( "%d \n",   x < y);

        printf( "%d \n",   x == y);

        printf( "%d \n",   x != y);

        printf( "%d \n",   x >= y);

        printf( "%d \n",   x <= y);
				
        return(0);
}



